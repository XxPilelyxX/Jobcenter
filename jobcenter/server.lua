local Tunnel = module("vrp", "lib/Tunnel")
local Proxy = module("vrp", "lib/Proxy")

vRP = Proxy.getInterface("vRP")
vRPclient = Tunnel.getInterface("vRP", "jobcenter")

RegisterNetEvent("pil:addjob")
AddEventHandler("pil:addjob", function(job)
    local source = source
    local user_id = vRP.getUserId({source})
    local arbejde = vRP.getUserGroupByType({user_id,"job"})
    vRP.addUserGroup({user_id, job})
    TriggerClientEvent('mythic_notify:client:SendAlert', source, { type = 'success', text = 'Du har nu fået jobbet '..job..'.'}, 5000)
end)